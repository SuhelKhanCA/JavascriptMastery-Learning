// rules for naming variables 

// you cannot start with number 
// example :- 
// 1value (invalid)
// value1 (valid)

var value1 = 2; // valid
console.log(value1);

// you can use only undersore _ or dollar symbol 
// first_name (valid)
// _firstname (valid) 
var _firstname = "Suhel Khan"; // valid
// first$name (valid)
// $firstname (valid)

// you cannot use spaces // ex: 'first name'
var first_name = "harshit"; // snake case writing 
var firstName = "harshit"; // camel case writing 
// first name (invalid)

